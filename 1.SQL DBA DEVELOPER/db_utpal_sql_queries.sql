RETURNS @TABLE_VAR  TABLE (SEQVALUE VARCHAR(30))
AS
BEGIN
DECLARE @COUNTER INT = @START
WHILE @COUNTER <= @END
	BEGIN
	INSERT INTO @TABLE_VAR VALUES ('SQL' + CONVERT(VARCHAR(30), @COUNTER))
	SET @COUNTER = @COUNTER + 1
	END 
RETURN 
END


