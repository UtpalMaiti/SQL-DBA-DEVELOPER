CREATE FUNCTION FNREPORTSEQUENCEVALUES (@START INT, @END INT)
RETURNS @TABLE_VAR  TABLE (SEQVALUE INT)	-- TABLE VARIABLES ARE STORED IN TEMPDB. 
AS
BEGIN
DECLARE @COUNTER INT = @START
WHILE @COUNTER <= @END							-- AS LONG AS; LOOP;
	BEGIN
